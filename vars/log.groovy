def info(message) {
    echo "INFO: ${message}"
}

def warning(message) {
    echo "WARN: ${message}"
}

def debug(message) {
    echo "DEBUG: ${message}"
}

def error(message) {
    echo "ERROR: ${message}"
}

def trace(message) {
    echo "TRACE: ${message}"
}

def fatal(message) {
    echo "FATAL: ${message}"
}
